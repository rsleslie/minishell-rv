#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 0 && n <= 9)
		ft_putchar_fd ((n + '0'), fd);
	else
	{
		if (n > 9)
		{
			ft_putnbr_fd((n / 10), fd);
			n = n % 10;
		}
		ft_putchar_fd((n + '0'), fd);
	}
}
